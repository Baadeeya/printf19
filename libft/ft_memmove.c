#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	size_t		i;
	const char	*psrc;
	char		*pdst;

	i = -1;
	psrc = src;
	pdst = dst;
	if (dst == 0 && src == dst)
		return (0);
	if (pdst < psrc)
		while (++i < len)
			pdst[i] = psrc[i];
	else
		while (len--)
			pdst[len] = psrc[len];
	return (dst);
}
